/**
relational operators : True(1) or False(0) [used for decision making]
          ==
          !=
          <
          >
          <=
          >=
**/
#include<stdio.h>
#include<stdio.h>
int main()
{
    int x=5,y=7;
    printf("%d\n",x);
    printf("%d\n",y);
    printf(".........\n");
    printf("x == y : %d\n",x==y);
    printf("x != y : %d\n",x!=y);
    printf("x < y : %d\n",x<y);
    printf("x > y : %d\n",x>y);
    printf("x <= y : %d\n",x<=y);
    printf("x >= y : %d\n",x>=y);
    return 0;

}
