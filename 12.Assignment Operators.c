/**
assignment operators :
         =
        +=   |....
        -=   |these 5 are named as
        *=   |Arithmetic Assignment Operators
        /=   |in some books;
        %=   |....
**/
#include<stdio.h>
int main()
{
    int x = 10;
    printf("%d\n",x);
    printf("%d\n",x+=5);
    printf("%d\n",x-=2);
    printf("%d\n",x*=7);
    printf("%d\n",x/=9);
    printf("%d\n",x%=6);


    return 0;
}

